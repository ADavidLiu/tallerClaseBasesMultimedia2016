    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String buscarpor;
        buscarpor = jTextField8.getText();
        boolean conexion;
        objbases = new BaseDatos();
        try {
            conexion = objbases.crearConexion();
            if (conexion) {
                arr = objbases.buscarCodigo(buscarpor);

                if(!arr.isEmpty()){
                
                jTextField1.setText(arr.get(0).toString());
                jTextField2.setText(arr.get(2).toString());
                jTextField3.setText(arr.get(1).toString());
                jTextField4.setText(arr.get(3).toString());
                jTextField5.setText(arr.get(4).toString());
                jTextField6.setText(arr.get(5).toString());
                jTextField7.setText(arr.get(6).toString());
                jLabel11.setIcon(new javax.swing.ImageIcon((Image)arr.get(7)));
                
                estudiante = new Estudiante(arr.get(0).toString(), arr.get(2).toString(), arr.get(1).toString(), arr.get(3).toString(), arr.get(4).toString(), arr.get(5).toString(), arr.get(6).toString(), arr.get(7).toString());
                
                }else{
                    JOptionPane.showMessageDialog(rootPane, "El Estudiante no se encuentra registrado!!!!. Primero ejecute la opci贸n de registro.");
                }
            }
        } catch (IOException ex) {
            Logger.getLogger(MostrarEstudiante.class.getName()).log(Level.SEVERE, null, ex);
        }
              
    }//GEN-LAST:event_jButton1ActionPerformed

